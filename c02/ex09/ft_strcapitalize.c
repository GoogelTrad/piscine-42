#include <stdio.h>

char	*ft_strcapitalize(char *str)
{
	int		maj;
	char	*p;

	maj = 1;
	p = str;
	while (*p != '\0')
	{
		if (*p >= 'a' && *p <= 'z' && maj == 1)
			*p -= 32;
		if (*p >= 'A' && *p <= 'Z' && maj == 0)
			*p += 32;
		maj = 0;
		if (*p < 'A' || *p > 'Z'
			|| *p < 'a' || *p > 'z'
			|| *p < '0' || *p > '0')
			maj = 1;
		p++;
	}
	return (str);
}
