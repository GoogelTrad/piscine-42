int ft_atoi(char *str)
{
	int	i;
	int pair;
	int nb;

	i = 0;
	pair = 1;
	nb = 0;
	while(str[i] == ' ')
		i++;
	while (str[i] == '+' || str[i] == '-')
	{
		if (str[i] == '-')
			pair = pair * -1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		nb = nb + (str[i] - 48);
		nb = nb * 10;
		i++;
	}
	nb = nb / 10;
	return (nb * pair);
}
